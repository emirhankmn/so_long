#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*tmp;
	t_list	*new;
	void	*src;

	if (lst == NULL || f == NULL )
		return (NULL);
	tmp = NULL;
	while (lst)
	{
		src = (*f)(lst->content);
		new = ft_lstnew(src);
		if (!new)
		{
			del(src);
			ft_lstclear(&tmp, del);
			return (NULL);
		}
		ft_lstadd_back(&tmp, new);
		lst = lst->next;
	}
	return (tmp);
}
